          <h1>Software dannosi</h1>
          <p>"Malware" è il termine generale per descrivere un software dannoso che si fa strada nei computer delle persone.
            Può diffondersi rapidamente, soprattutto via e-mail, servizi online, file sharing peer-to-peer e programmi di messaggistica istantanea. Il malware si presenta in diverse forme: il più distruttivo può rubare o cancellare dati da un disco rigido o disabilitare completamente un computer.<br><br>
            Tra i più noti ci sono:
          </p>
          <ul>
            <br>
            <li><b>Virus</b>: qualsiasi programma che si infiltra in un computer e poi lo fa funzionare male come se fosse "malato" (ad esempio rallentarlo).</li>
            <br>
            <li><b>Spyware</b>: un programma segreto che osserva e segnala il comportamento online di un utente, a sua insaputa, di solito per scopi commerciali e può anche modificare le impostazioni su un computer o visualizzare annunci pop-up.</li>
            <br>
            <li><b>Spam</b>: messaggi di posta elettronica indesiderati trasmessi a più persone, di solito per scopi pubblicitari. Sebbene spesso innocui, possono anche portare dei virus.</li>
            <br>
            <li><b>Worm</b>: un programma che invade i computer in una rete e si nasconde in profondità all'interno del software. Si replica per impedire la cancellazione e può trasportare un virus o interferire con le operazioni del computer.</li>
            <br>
            <li><b>Trojan</b>: un programma che entra in un computer nascondendosi all'interno di un'applicazione apparentemente innocente. Una volta all'interno, potrebbe prendere il controllo del tuo computer e rubare informazioni sensibili come password e dettagli della carta di credito.</li>
            <br>
            <li><b>Botnet</b>: un grande gruppo di computer sotto il controllo di una banda criminale che li ha infettati, all'insaputa dei loro proprietari. Possono essere utilizzati per scopi criminali.</li>
            <br>
            <li><b>Fishing</b>: un metodo per indurre le persone a rivelare informazioni sensibili, come dettagli bancari o password. Di solito assume la forma di un'e-mail che sembra un messaggio autentico.</li>
            <br>
            <li><b>Ransomware</b>: un tipo di malware che impedisce all'utente di accedere ai propri file e dispositivi crittografandoli, e quindi richiedendo un pagamento anonimo online per riottenere l'accesso.</li>
            <br>
            <li><b>Adware</b>: anche definito come virus pubblicitario. Non è difficile notare la sua presenza poiché è nato per essere fastidioso. Crea continuamente avvisi pubblicitari e finestre e rende la navigazione su Internet frustrante e stancante.</li>
          </ul>


